                    <h1>Porque fazer cursinho?</h1>
                    <div class="artigo-data">Escrito em 02/02/2014</div>
                    
                    <p>Ingressar em uma universidade pública ou privada de renome é o sonho de muitos 
                    jovens e pais. No Brasil, muitos estudantes que concluem o ensino médio não 
                    estão preparados para prestar as provas do ENEM e também do vestibular.</p>
                    
                    <p>Aí entra a figura do cursinho. Ele surge como uma ferramenta para suprir as carências do 
                    Ensino Médio, já que consegue concentrar o conteúdo das principais disciplinas em aulas 
                    especializadas. Os estudantes acabam nivelados e percebem quais são seus pontos fortes e 
                    fracos.</p>
                    
                    <p>O cursinho coloca o estudante em contato com o conteúdo cobrado pelos principais vestibulares 
                    do país, usando material didático atualizado, conteúdo especializado, plantões de dúvidas, 
                    simulados, palestras eprofessores qualificados.</p>
                    
                    <p>É isso que acontece no Projeto Chance. O aluno entra em contato com a pluralidade cultural por 
                    meio de saraus, palestras, debates, rodas de leitura, aulas sobre problemas brasileiros, meio 
                    ambiente, debates, atividades artísticas, culturais e sociais. A importância do Projeto Chance é 
                    ainda maior quando lembramos que o público-alvo é composto por alunos das classes C e D, que não 
                    tiveram oportunidade de frequentar as melhores escolas do país e concorrer em igualdade.</p>
                    
                    <p>Talvez um dia exista no Brasil outra forma de acesso às universidades, sem a necessidade da 
                    concorrência, da exclusão. Por enquanto, o vestibular é a única forma. O cursinho é a 
                    alternativa mais eficaz para preparar os estudantes para este desafio tão fundamental 
                    para o futuro profissional.</p>
                    
                    <p>Misael Silva é diretor do Projeto Chance, uma instituição sem fins lucrativos que tem o objetivo 
                    de unir educação e inclusão social.</p>
                    
