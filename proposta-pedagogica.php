                    <h3>c) Aula ambiente</h3>
                    <p>A aula ambiente é multidisciplinar.  Ela alia os conhecimentos adquiridos em sala de aula à 
                    observação da realidade. O objetivo desse encontro é que o estudante compreenda a formação 
                    de um determinado espaço, por meio da paisagem, relevo, rios, acumulação de tempos 
                    desiguais registrados na arquitetura, diversidade artística e cultural dos lugares visitados 
                    e faça uma abordagem onde a história se amarra à geografia e vice-versa.</p>

                    <h3>d) Rodas de leitura</h3>
                    <p>As Rodas de Leitura acontecem no mês de setembro de cada ano. São dedicadas ao estudo dos 
                    livros exigidos nos vestibulares da Fuvest e Unicamp. O objetivo do encontro é fazer com 
                    que o estudante aprofunde o conhecimento das obras, por meio de observações sobre os textos, 
                    os autores, excertos, histórias, citações dos personagens, características estéticas e 
                    linguagem literária.</p>

                    <h3>e) Sarau ambiente</h3>
                    <p>O Sarau Ambiente acontece no mês de agosto de cada ano e tem como objetivo ampliar a 
                    formação cultural do estudante. No sarau a leitura e a criatividade de cada um são 
                    estimuladas. São valorizados os talentos, a soma de conhecimento, a troca de experiências, 
                    as descobertas e o convívio estudantil. Acreditamos que o Sarau Ambiente é de vital 
                    importância para o estudante se expressar e se manifestar artisticamente, por meio de suas 
                    habilidades. Durante o Sarau Ambiente as pessoas são convidadas a trazer livros para uma 
                    grade troca, onde todos saem ganhando.</p>
        
