                    <h1>II CAMINHADA PELA LEITURA EM PARAISÓPOLIS</h1>
                    <p class="p_subTit">A importância da leitura na vida de um povo</p>
                    <div class="artigo-data">Escrito em 15/03/2014</div>
                    
                    <p>Você gosta de ler? Se a sua resposta for sim, parabéns, você é um cidadão que está incluído 
                    no mundo, transforma, faz diferença. Se esse ainda não é o seu caso, está na hora de parar e 
                    pensar.</p>

                    <p>O Brasil ainda tem altas taxas de analfabetismo. Muitas pessoas que completam o ensino médio 
                    não conseguem compreender corretamente o significado de textos que leem, isto quanto conseguem 
                    ler (são os analfabetos funcionais). São os problemas decorrentes da precariedade do ensino 
                    público e porque não citar, da falta de bibliotecas nas escolas.</p>

                    <p>A leitura tem quem se tornar um hábito comum na vida dos estudantes e do povo brasileiro. 
                    Ela deve ser encarada como um hobby, não apenas como uma obrigação escolar. É preciso ir além do 
                    que o professor pede.</p>

                    <p>Ler tem que ser tão bom como assistir TV, escutar musica, descansar, reunir-se com amigos e com a 
                    família. Quem gosta de ler conhece seus direitos como cidadão, tem boa comunicação, escreve bem, 
                    tira boas notas na escola e cria consciência do mundo.</p>

                    <p>Como leitor, precisamos estimular novos leitores. O conhecimento adquirido da leitura de um livro, 
                    jornal, revista tem que ser transmitido. O hábito de ler tem que ser estimulado desde cedo para que 
                    se torne uma fonte inesgotável de conhecimento para a vida.</p>

                    <p>Hoje, infelizmente, o índice médio de livros lidos por pessoa, por ano no Brasil é de apenas dois 
                    livros, segundo pesquisa realizada pela Câmara Brasileira do Livro. Isso é muito triste, é muito 
                    pouco! Um livro não pode ficar em uma prateleira como enfeite ou ser devorado pelas traças, mas por 
                    leitores apaixonados que querem conhecimento.</p>

                    <p>Vivemos em uma sociedade que está em constante transformação. Nesse contexto, a leitura é mais que 
                    necessária para a construção de um mundo mais justo e inclusivo.  Com pessoas estudando além da 
                    escola, freqüentado bibliotecas e inseridas no universo da cultura. Ler por prazer, para estudar e 
                    para se informar são caminhos para a verdadeira mudança social.</p>
                    
