                    <h1>A importância de estudar</h1>
                    <div class="artigo-data">Escrito em 02/02/2014</div>
                    
                    <p>Você já deve ter ouvido a frase: “você tem que estudar para ser alguém na vida”. 
                    Pois é... Para muita gente, isso não passa de uma bobagem. Mas basta prestar um 
                    pouquinho de atenção ao mundo a sua volta para entender que o estudo é uma pedra que, 
                    de tão preciosa, não tem preço!</p>
                    
                    <p>Mas o que é ESTUDAR? É muito mais do que simplesmente concluir o ensino médio, 
                    passar no vestibular, fazer faculdade ou conseguir um bom emprego.O ESTUDO é algo 
                    que fazemos com alegria e com prazer. É descobrir um mundo que estava encoberto, 
                    escondido nas páginas que poucos tiveram acesso a ler. É um ato de liberdade que 
                    abre portas por onde você jamais imaginaria entrar.</p>
                    
                    <p>Vivemos em uma sociedade que cobra mais do que conhecimento. Ela exige uma boa formação 
                    também. Quem estuda, participa melhor da vida social e política da comunidade. E faz 
                    diferença no mundo onde vive.</p>
                    
                    <p>Como estudantes, devemos refletir de que maneira estamos empregando o conhecimento que 
                    adquirimos e também sobre o nosso papel na sociedade. Quanto mais estudamos, mais nos 
                    tornamos livres para tomar decisões.</p>
                    
                    <p>São muitas as fórmulas para adquirir conhecimento.O Projeto Chance trabalha unindo educação 
                    e inclusão social.Quando estudamos com prazer, com alegria e buscando a nossa satisfação, 
                    fica muito mais fácil atingir nossos objetivos... E passar no vestibular fica muito mais 
                    fácil!</p>
                    
