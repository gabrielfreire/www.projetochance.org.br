        <h1>Agronomia</h1>
        
        <p><b>São as ciências e técnicas usadas para melhorar a qualidade e a produtividade de lavouras, rebanhos e produtos agroindustriais.</b> O agrônomo planeja, organiza e acompanha o preparo e o cultivo do solo, o combate a pragas e doenças, a colheita, o armazenamento e a distribuição da safra. Cuida da alimentação, da reprodução, da saúde e do abate de animais. Também gerencia a industrialização, o armazenamento e a comercialização de alimentos de origem animal e vegetal. Além de desempenha funções em escritórios, informando-se sobre novas tecnologias e pesquisas científicas da área, calculando estoques e checando na internet a cotação dos produtos nas bolsas de valores internacionais.
        
        <h4>O Mercado</h4>
        <p>O setor agropecuário brasileiro está em sua melhor fase de produção de grãos, abrindo muitas perspectivas para esse profissional, especialmente em empresas de semente e agroquímicos como Monsanto, Bunge e Cargill. A pecuária também absorve o profissional, que atua na produção, no manejo e na recuperação de pastagens.</p> 
        
        <h4>O Curso</h4>
        <p>Os dois primeiros anos mesclam matérias das áreas de ciências biológicas e exatas, como biologia, bioquímica, matemática, informática e estatística. Nos três anos seguintes, o forte são as disciplinas profissionalizantes, ministradas nas subáreas de engenharia rural, ciência do solo, agricultura e engenharia florestal, entre outras. Boa parte da carga horária é dedicada a aulas práticas em laboratórios e fazendas experimentais.</p>
        <p><b>Duração média: </b>Cinco anos</p> 
        
       
        <h4>O que você pode Fazer</h4>        
        
        <h2>Defesa Sanitária</h2>
        <p>Combater pragas e prevenir doenças em lavouras e rebanhos.</p>
        <h2>Economia e administração agroindustrial</h2>
        <p>Planejar e gerenciar as operações de distribuição e venda de produtos agrícolas. Coordenar programas de crédito rural para cooperativas e pequenos produtores.</p>
        <h2>Engenharia rural</h2>
        <p>Projetar e supervisionar obras em propriedades rurais, como construções, nivelamento do solo, montagem de sistemas de irrigação e de drenagem.</p>
        <h2>Ensino</h2>
        <p>Lecionar em escolas públicas ou particulares de educação profissional ou em faculdades.</p>
        <h2>Fitotecnia</h2>
        <p>Acompanhar o cultivo e a colheita de safras, buscando aumentar a produtividade por meio da seleção de sementes, do emprego de fertilizantes e adubos e do combate a doenças e pragas.</p>
        <h2>Indústria e comercialização de alimentos</h2>
        <p>Supervisionar e gerenciar a qualidade e a estratégia de preços de alimentos de origem animal e vegetal.</p>
        <h2>Manejo ambiental</h2>
        <p>Explorar racionalmente os recursos naturais, preservando o meio ambiente.</p>
        <h2>Melhoramento animal e vegetal</h2>
        <p>Realizar pesquisas e desenvolver técnicas visando à melhoria da produção.</p> 
        <h2>Produção agroindustrial</h2>
        <p>Gerenciar o processo de industrialização de produtos agrícolas, controlando a qualidade final da produção. Pesquisar novas tecnologias e produtos.</p> 
        <h2>Silvicultura</h2>
        <p>Recuperar matas devastadas e cuidar do plantio e do manejo de áreas de reflorestamento. Preparar relatórios de impacto ambiental.</p>   
        <h2>Solos</h2>
        <p>Preservar a fertilidade e controlar as propriedades físicas dos solos, prescrevendo seu manejo.</p>    
        <h2>Zootecnia</h2>
        <p>Controlar a produção animal, cuidando da alimentação, da saúde, da reprodução e da qualidade dos rebanhos</p>
        
