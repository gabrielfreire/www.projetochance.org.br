        <h1>Biotecnologia</h1>
        <p><b>É a aplicação de conhecimentos químicos e biológicos e de novas tecnologias nas áreas da saúde, de alimentos, química e ambiental.</b> O profissional de Biotecnologia é multidisciplinar, pois entende tanto de biologia quanto de química, física, estatística e informática. Em laboratório, ele estuda o melhoramento genético, a criação e o gerenciamento de novos produtos, que podem ser medicamentos, ingredientes para alimentos industriais ou até mesmo uma planta. Na área da microbiologia, estuda fungos, bactérias, vírus e protozoários e as moléstias que eles causam em plantas e no organismo do homem e de animais, além de pesquisar métodos de utilização desses microrganismos na produção de alimentos e bebidas, como laticínios, cerveja e vinho. </p>
        <h4>Dúvida do Vestibulando</h4>
        <p><i>Qual é a diferença entre fazer um bacharelado e um tecnológico?</i></p>
        <p>A graduação tecnológica forma especialistas em gestão e processos biotecnológicos. O bacharelado propõe formação abrangente para que o profissional possa exercer atividades em todas as áreas da biotecnologia, como o desenvolvimento de tecnologias e aperfeiçoamento dos processos biológicos, bem como se especializar em algum setor de sua preferência, como agrícola ou ambiental. </p>
        <h4>O Mercado </h4>
        <p>A demanda por bacharéis e tecnólogos deve continuar crescendo. De acordo com estimativas da Organização das Nações Unidas para Alimentação e Agricultura (FAO), a população mundial chegará a 9 bilhões até 2050. É preciso, segundo a entidade, aumentar a produção de alimentos em 70% para dar conta de tanta gente.</p> 
	<h4>O Curso</h4>
        <p>O curso de bacharelado nem sempre têm o mesmo direcionamento e, por isso, podem dar ênfase a um ou outro campo da biotecnologia. Eles também têm nomes diferentes, como Biotecnologia (UFSCar), Engenharia de Bioprocessos (UFRJ), Engenharia Bioquímica (USP-Lorena), Engenharia de Bioprocessos e Biotecnologia (UFPR e a Uergs), Ciências Biológicas - ênfase em biotecnologia (Univali).</p>
        <p><b>Duração média: </b>de quatro a cinco anos </p>
        <h4>O que você pode Fazer</h4>
        <h2>Meio Ambiente</h2>
        <p>Estudar processos biotecnológicos para a recuperação de solos e o aprimoramento da agricultura. Pesquisar a poluição e a contaminação do ar, da água e do solo por microrganismos.</p>
        <h2>Alimentos e Bebidas</h2>
        <p>Acompanhar a produção de alimentos e bebidas que levem microrganismos em sua composição, como queijos e cervejas.</p>
        <h2>Saúde</h2>
        <p>Pesquisar o uso de microrganismos na produção de medicamentos e vacinas. Identificar micróbios causadores de doenças em laboratórios de análises clínicas e institutos de pesquisa. Atuar na prevenção, no controle e no combate a infecções hospitalares.</p>
        <h2>Agronegócios</h2>
        <p>Aprimorar técnicas de combate a pragas e doenças nas lavouras e nos rebanhos.</p>
        
        
        
        <br /><br />
        <h1>Ciência dos Alimentos</h1>
        <p><b>É o estudo do alimento sob os aspectos físicoquímicos, microbiológicos e nutricionais, da produção ao consumo.</b> O bacharel em Ciências dos Alimentos gerencia o alimento desde a produção agrícola, a pós-colheita, a pós-ordenha, o pós-abate, passando pelo abastecimento, conservação, transporte, atacado e varejo e controle de qualidade do que o consumidor vai colocar no prato.</p>
        <h4>Dúvida do Vestibulando</h4>
        <p><i>Qual a diferença entre Ciência dos alimentos e Engenharia de alimentos?</i></p>
        <p>A função do engenheiro de alimentos é desenvolver fórmulas e métodos que garantam a utilização saudável das matérias do princípio até o final da cadeia de produção. O profissional de Ciências dos Alimentos tem como tarefa acompanhar a execução dessas técnicas, averiguando todo o processo de transformação dos produtos. </p>
        <h4>O Mercado</h4>
        <p>O Brasil é um dos principais produtores mundiais de alimentos. Além disso, com a melhora no poder aquisitivo, a população compra mais comida - só nos primeiros cinco meses de 2010, os brasileiros compraram 20% a mais de itens de produtos alimentícios que o mesmo período de 2009, segundo levantamento da Kantar World Panel, empresa mundial de pesquisas sobre consumo. Por isso, a procura por profissionais na área de alimentos tende a crescer, tanto para a produção agrícola, em fazendas, como em indústrias processadoras de alimentos, centrais de abastecimento e distribuição.</p>
        <h4>O Curso</h4>
        <p>O primeiro ano é de formação básica, com disciplinas como matemática, química, português, biologia, bioquímica e estatística. No segundo ano, são introduzidas algumas matérias profissionalizantes, como introdução a ciências dos alimentos, química de alimentos, nutrição básica e logística de alimentos. Os dois últimos anos são recheados de disciplinas direcionadas à profissão e às atividades práticas, realizadas em laboratórios ou nas próprias.</p>
        <p><b>Duração Média:</b> Quatro anos</p>
        <h4>O que você pode Fazer?</h4>
        <h2>Pesquisa</h2>
        <p>Pesquisar e desenvolver novos produtos alimentícios, de origem vegetal ou animal.</p>
        <h2>Controle de Qualidade</h2>
        <p>Desenvolver métodos de controle de qualidade alimentar de acordo com aspectos higiênico sanitários, físico-químicos e nutricionais.</p>
        <h2>Conservação</h2>
        <p>Criar embalagens e métodos de processamento que garantam maior vida útil aos alimentos.</p>
        <h2>Consultoria</h2>
        <p>Prestar consultoria nas áreas de alimentos e nutrição, desde a capacitação de pessoal, qualificação de fornecedores, auditorias, marketing e adequação às normas e legislação vigentes.</p>
        <h2>Educação</h2>
        <p>Realizar programas de educação para o consumo de alimentos.</p>
        
