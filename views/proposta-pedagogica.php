        universo da cultura, ser autônomo, pensante e ter capacidade para entender criticamente 
        os acontecimentos e a realidade da qual faz parte. O material didático, adotado pelo 
        Projeto Chance, procura atender esta finalidade. O enriquecimento cultural e erudito do 
        estudante faz toda a diferença na hora de estudar. E é nisto que acreditamos: ir além das 
        aulas tradicionais, ajudando no desenvolvimento pré-universitário do aluno.</p>
        
