        <h1>História do Vestibular</h1>
        <br />
        <p><b>Ano de 1808</b> - O ingresso nos cursos superiores introduzidos no Brasil se fazia mediante "exames preparatórios"; a partir de 1837 esse ingresso torna-se privilégio de egressos de alguns colégios.</p>
        <p><b>Ano de 1911</b> - Torna-se obrigatório o exame de seleção para o ingresso em curso superior pela Lei Orgânica do Ensino Superior e do Fundamental/ decreto 8659 de 5/4/1911; por este decreto estabeleceu-se a exigência de exame de admissão e formularam-se critérios relativos à forma do exame, à banca examinadora, às datas dos exames e às taxas de inscrição;</p>
        <p><b>Ano de 1915</b> - O Decreto 11530 de 18/3/1915 cria a denominação (usada até hoje) do exame de admissão aos cursos superiores para exames vestibulares.</p>
        <p><b>Ano de 1946</b> - Escola Paulista de Medicina (hoje Unifesp) introduz testes em provas internas (pelo prof. Walter Leser - que viria depois a fundar o CESCEM)</p>
        <p><b>Ano de 1964</b> - É criado o CESCEM: vestibular da área de Biológicas em São Paulo; utilização de testes de múltipla-escolha.</p>
        <p><b>Ano de 1967</b> - É criado o CESCEA: vestibular da área de Humanas em São Paulo; também com testes.</p>
        <p><b>Ano de 1969</b> - Criado o MAPOFEI: vestibular da área de Exatas (questões discursivas).</p>
        <p><b>Ano de 1975</b> - A USP unifica o programa de vestibular para todas as áreas.</p>
        <p><b>Ano de 1976</b> - A USP cria a Fuvest (exame em duas fases: a 1ª com testes e a 2ª com questões dissertativas); extintos os vestibulares por área.</p>
        <p><b>Ano de 1977</b> - A Fuvest passa a realizar os exames da USP e também da Unicamp e Unesp.</p>
        <p><b>Ano de 1981</b> - A Unesp passa a realizar separadamente seus exames. 1987 - A Unicamp passa a realizar separadamente seus exames (em 2 fases, somente com questões discursivas; todas as matérias têm o mesmo peso).</p>
        <p><b>Ano de 1987</b> - A Fuvest passa a realizar provas de 2ª fase em níveis diferentes conforme matéria e carreira; melhora muito o rendimento dos candidatos de Humanas em Física, Química e Biologia.</p>
        <p><b>Ano de 1988</b> - A Fuvest aproxima os pesos das matérias; aumenta a necessidade de uma boa formação geral. 1990 - A Fuvest reduz para 80 o número de questões na 1ª fase e aumenta em 60% os convocados para a 2ª fase. Notas mínimas fazem sobrar vagas na USP.</p>
        <p><b>Ano de 1991</b> - A Fuvest reduz para 72 o número de questões na 1ª fase e retira a exigência de notas mínimas.</p>
        <p><b>Ano de 1992</b> - Encontro internacional sobre os vestibulares, em Durblin, na Irlanda; o Etapa é a única escola brasileira a enviar participantes.</p>
        <p><b>Ano de 1993</b> - A Fuvest retira totalmente a exigência de notas mínimas para o vestibular de 94.</p>
        <p><b>Ano de 1994</b> - A Fuvest muda o vestibular 95: na 1ª fase, aumentando o número de questões e na 2ª fase, reduzindo o número de provas; a Unicamp reduz o peso da Redação e introduz pesos diferenciados na 2ª fase.</p>
        <p><b>Ano de 2000</b> - Exame do Enem passa a contar nas notas da Fuvest, Unicamp e Unesp.</p>
        <p><b>Ano de 2003</b> - Fuvest reduz para 100 questões em um único dia a sua 1ª fase.</p>     
        <p><b>Ano de 2004</b> - Unicamp decide que a partir do vestibular de 2005 serão corrigidas as redações somente dos candidatos que obtiverem pelo menos 50% dos pontos nas questões da 1ª fase.</p>
        <p><b>Ano de 2005</b> - O vestibular da Unicamp introduz bônus de 30 pontos (numa média de 540) para estudantes vindos do ensino médio em escola pública e, dentre estes, mais 10 pontos caso se declarem negros ou indígenas.</p>
        <p><b>Ano de 2007</b> - A USP e Unificam apresentam uma lista unificada de leitura obrigatória para o vestibular; A USP reduz a primeira fase para 90 questões e introduz um bônus de 3% a mais nas notas para estudantes vindos de escolas públicas.</p><br />
