        <h1>Quem somos</h1>
        
        <p>O <strong>PROJETO CHANCE</strong> é uma instituição sem fins lucrativos que tem o objetivo de unir educação e 
        inclusão social. Ele funciona bem no coração da comunidade de Paraisópolis, em São Paulo e 
        dá aulas gratuitas para estudantes carentes. Por meio destas aulas, os alunos se preparam para 
        as provas do Enem e também para o vestibular. O <em>Chance</em> surgiu a partir de outro projeto, o 
        Mackvest, criado por estudantes da Escola de Direito da Universidade Presbiteriana Mackenzie.</p>

        <p>Temos orgulho de trabalhar como agentes de transformação social por meio da educação. 
        Desde a fundação do projeto, em 2007, o <em>Chance</em> cresceu e ganhou reconhecimento. 
        Foi vencedor do prêmio Dolce Vita Amigo do Morumbi – Categoria, Empreendedor Social Ação 
        em Educação & Cultura, organizado pela Revista Dolce Vita (2009) e Prêmio IAM - 
        Iniciativa Jovem, promovido pela Universidade Anhembi Morumbi e o Grupo Laureate 
        Universidades (2010).</p>
        
        <h4>Nossa história</h4>

        <p>Em 2006, um grupo de estudantes de direito da Universidade Presbiteriana Mackenzie se 
        reuniu para dar aulas gratuitas na comunidade de Paraisópolis, São Paulo. 
        O objetivo era preparar os estudantes para o vestibular da Universidade Mackenzie. 
        Vendo que a demanda era grande e que havia espaço para iniciativas como esta, o professor 
        de história do Brasil, Misael Silva, teve a idéia de ampliar a iniciativa para que os 
        jovens tivessem a chance de ingressar em qualquer Universidade do país. Nasce assim, em 2008, 
        o Projeto Chance, voltado para a educação, baseado na pluralidade cultural, sem fins lucrativos 
        e que trabalha a inclusão social. Ele é mantido com a ajuda de professores voluntários.</p>

        <p>Três anos depois, a primeira sede foi criada no bairro de Paraisópolis, funcionando como 
        um espaço para atendimento dos alunos e para as matrículas. Essa conquista não foi apenas a 
        de ter um espaço próprio, mas significou uma mudança de conceito. O Projeto passou a incluir 
        em sua grade de disciplinas, aulas focadas nos problemas brasileiros e, também, caminhadas 
        pela leitura. Ao mesmo tempo, o projeto se firmou como um curso preparatório para o ENEM e 
        para o vestibular, ajudando o desenvolvimento pré-universitário do estudante por meio de 
        atividades artísticas, culturais e sociais. Desde 2012, o Projeto Chance ministra suas aulas 
        na ETEC (Escola Técnica Estadual Abdias do Nascimento) graças a uma parceria firmada com o 
        governo do estado.</p> 
