        <h2>Quem  somos</h2> 
		<p>Fundado em 2007, pelo Professor Misael, o Projeto Chance é uma instituição filantrópica, sem fins lucrativos, 
        que visa à inclusão social de jovens  e adultos por meio da educação.</p>

		<h2>Onde atuamos</h2> 
		<p>O projeto Chance atua na cidade de São Paulo em uma das regiões que possui alto índice de vulnerabilidade social, 
        a comunidade de Paraísopolis, localizada no coração do Morumbi bairro nobre da capital.</p>

		<h2>Como atuamos</h2>
		<p>O projeto Chance é realizado por meio de trabalho voluntário e de parcerias. Promove cursinho pré-vestibular gratuito, 
        cursos de línguas, ações sociais, feiras de emprego, orientação vocacional e por meio do incentivo à leitura, o projeto 
        também realiza concursos literários.</p> 
        
		<h2>Porque atuamos</h2> 
		<p>O Projeto Chance acredita na educação como o veículo que conduz à transformação social do ser humano.</p>

		<h2>Missão</h2>
		<p>Desenvolver soluções capazes de melhorar o setor educacional, potencializar o conhecimento humano e fomentar a 
        pluralidade cultural.</p>

		<h2>Visão</h2> 
		<p>Ser o agente criativo e inovador de transformação social por meio da edificação de saberes e contribuição para a 
        sociedade mais justa e inclusiva.</p> 

		<h2>Valores</h2>
		<p>> Convívio estudantil saudável, fraterno e coletivo;<br />
		   > Responsabilidade institucional, compromisso social, transparência e ética;<br />
		   > Estimular o desenvolvimento intelectual e promoção humana.</p>
    
