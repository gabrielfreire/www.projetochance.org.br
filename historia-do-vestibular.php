                    <h1>História do vestibular</h1>                    
                    
                    <p>O vestibular surgiu no Brasil em 1911, por iniciativa de Rivadávia da Cunha 
                    Corrêa, político, que ocupava o cargo de Ministro da Justiça e dos Negócios 
                    Interiores. Nesse período, só cursava universidade pública estudante que vinha 
                    de um colégio tradicional.</p>
                    
                    <p>No início, os alunos faziam provas escritas e orais, que incluía questões de 
                    língua portuguesa, língua estrangeira, matemática, química e física. Além da 
                    exigência de conteúdos do primeiro ano de faculdade, os candidatos recorriam a 
                    aulas específicas, o que provocou o surgimento de cursinhos no país.</p>
                    
                    <p>Na década de 60 o vestibular das universidades federais era realizado no mesmo 
                    dia, fato que impedia o aluno de prestar outros vestibulares e concorrer a 
                    mais de uma vaga. Nesse período surge o exame unificado, uma única prova que 
                    valia para várias faculdades.</p>
                    
                    <p>Com a criação da Comissão Nacional do Vestibular Unificado, em 1970, os 
                    vestibulares passaram a ter datas distintas, com conteúdo apenas do ensino 
                    médio.</p>
                    
                    <p>Em 1976, a USP, Universidade de São Paulo, cria a Fuvest, que unifica os vestibulares da 
                    Unesp, Universidade Estadual Paulista e Unicamp, Universidade Estadual de Campinas. 
                    A unificação durou até 1981, quando a Unesp se desvinculou, em 1987, a Unicamp fez o mesmo. 
                    Mesmo com a desvinculação a Fuvest continua sendo o maior vestibular do Brasil. </p>
                    
                    <p>Em 1998, surge o ENEM, Exame Nacional do Ensino Médio, com a proposta de avaliar anualmente a 
                    qualidade do ensino médio no Brasil. Diferente dos vestibulares, o ENEM foca o conteúdo de 
                    forma interdisciplinar que procura avaliar a competência, não o conteúdo.</p>
                                        
                    <p>Dois anos depois, o exame do ENEM passa a contar como bônus nas notas da Fuvest, Unicamp e Unesp. 
                    Daí em diante, a maioria dos vestibulares no Brasil passou a adotar o ENEM em sua avaliação.</p>
                    
                    <p>Em 2007, Fuvest e Unicamp unificam a lista de livros de leitura obrigatória para o vestibular.</p>
                    
